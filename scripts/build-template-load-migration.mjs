/**
 * Generate the "load contract bodies" migration from the canonical tokenized
 * markdown in supabase/contract_templates/.
 *
 *   node scripts/build-template-load-migration.mjs
 *
 * The .md files are the editable source of truth; this emits a deterministic
 * migration that (1) loads each body into contract_templates.body and (2) derives
 * the per-template template_tokens rows (the concrete tokens each contract uses,
 * e.g. {{BUYER.FULL_NAME}}, linked to template_id). Re-run after editing any body.
 *
 * Bodies are dollar-quoted ($body$…$body$) so the legal text — quotes, $, □, ☐ —
 * needs no escaping.
 */
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const BODIES = resolve(HERE, '../supabase/contract_templates');
const OUT = resolve(HERE, '../supabase/migrations/20260629100000_load_contract_bodies.sql');

/** kind of a concrete token by its namespace. */
function kindOf(ns) {
  if (ns === 'SIG') return 'signature';
  if (ns === 'DOC') return 'system';
  return 'field';
}

/**
 * Templates whose contract_templates rows POSTDATE the migration-11 seed (the
 * liability releases, seeded by 20260701070000_liability_releases.sql). This
 * generated loader (20260629100000) runs BEFORE that migration on a fresh
 * database, so it must ensure the row exists before loading its body; the
 * INSERT is idempotent and 20260701070000 re-asserts the same rows (and owns
 * the contract_requirements matrix).
 */
const POST_SEED_TEMPLATES = {
  RELEASE_GENERAL:        { title: 'General Visitor Liability Release',                 parties: ['PARTICIPANT', 'GUARDIAN', 'COMPANY'] },
  RELEASE_PARTICIPANT:    { title: 'Participant Liability Release',                     parties: ['PARTICIPANT', 'GUARDIAN', 'COMPANY'] },
  RELEASE_HORSE_EXERCISE: { title: 'Horse Exercise Liability Release',                  parties: ['OWNER', 'COMPANY'] },
  RELEASE_HORSE_CARE:     { title: 'Horse Handling and Routine Care Liability Release', parties: ['OWNER', 'COMPANY'] },
  // Contract-module decomposition (20260701080000): the side-scoped Layer 2
  // transaction-representation module. service_type stays NULL (one tokenized
  // template serves purchase/sale/lease-in/lease-out representation).
  HORSE_TRANSACTION_REP:  { title: 'Horse Transaction Representation Agreement',        parties: ['CLIENT', 'COMPANY'] },
};

const files = readdirSync(BODIES).filter((f) => f.endsWith('.md')).sort();

let sql = `/*
  # FHE CRM — Load Contract Bodies (migration 17)

  GENERATED by scripts/build-template-load-migration.mjs from the tokenized
  markdown in supabase/contract_templates/. Do not edit by hand — edit the .md
  source and re-run the generator.

  Loads each tokenized body into contract_templates.body and derives the
  per-template template_tokens rows (the concrete tokens each contract uses,
  linked to template_id). Idempotent: clears derived per-template rows first.
*/

-- Clear previously-derived per-template token rows (global dictionary rows, where
-- template_id IS NULL, are left untouched).
DELETE FROM template_tokens WHERE template_id IS NOT NULL;
`;

for (const file of files) {
  const key = basename(file, '.md');
  const body = readFileSync(resolve(BODIES, file), 'utf8');

  // distinct concrete tokens in document order
  const tokens = [...new Set((body.match(/\{\{[A-Z0-9_.]+\}\}/g) ?? []))];

  sql += `\n-- ── ${key} ─────────────────────────────────────────────\n`;
  const meta = POST_SEED_TEMPLATES[key];
  if (meta) {
    // Row postdates migration 11's seed — ensure it exists (see POST_SEED_TEMPLATES).
    sql += `INSERT INTO contract_templates (template_key, title, service_type, party_namespaces)\n`
      + `  VALUES ('${key}', '${meta.title.replace(/'/g, "''")}', NULL, ARRAY[${meta.parties.map((p) => `'${p}'`).join(',')}])\n`
      + `  ON CONFLICT (template_key) DO NOTHING;\n`;
  }
  sql += `UPDATE contract_templates SET body = $body$${body}$body$, updated_at = now()\n  WHERE template_key = '${key}';\n`;

  if (tokens.length) {
    sql += `INSERT INTO template_tokens (template_id, namespace, field, token, kind, required, party_scoped)\n`;
    const rows = tokens.map((tok) => {
      const parts = tok.slice(2, -2).split('.');
      const ns = parts[0];
      const field = parts.slice(1).join('.');
      return `  SELECT id, '${ns}', '${field}', '${tok}', '${kindOf(ns)}', false, false FROM contract_templates WHERE template_key = '${key}'`;
    });
    sql += rows.join('\n  UNION ALL\n') + ';\n';
  }
}

writeFileSync(OUT, sql);
console.log(`wrote ${OUT} (${files.length} templates)`);
